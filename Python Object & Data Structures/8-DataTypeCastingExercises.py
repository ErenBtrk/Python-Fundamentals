#Circle Area Formula : πr²
#Circle Circumference Formula : 2πr

Radius = float(input("Please enter radius for circle: "))
PiNumber = 3.14

Area = PiNumber * Radius**2
Circumference = 2 * PiNumber * Radius

print("Area is :",Area)
print("Circumference is :",Circumference)
